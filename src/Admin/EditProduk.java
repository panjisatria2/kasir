    private void viewCategory() {
        try {
            Connection C = Koneksi.Gas();
            Statement ST = C.createStatement();
            String Q = "SELECT id,nama FROM produk_kategori";
            ResultSet R = ST.executeQuery(Q);
            categorip.removeAllItems();
            while (R.next()) {
                int id = R.getInt("id");
                String name = R.getString("nama");
                categorip.addItem(id + "-" + name);
            }
        } catch (Exception e) {
        }
    }

    private void viewSupplier() {
        try {
            Connection C = Koneksi.Gas();
            Statement ST = C.createStatement();
            String Q = "SELECT id,nama FROM supplier";
            ResultSet R = ST.executeQuery(Q);
            suplier.removeAllItems();
            while (R.next()) {
                int id = R.getInt("id");
                String name = R.getString("nama");
                suplier.addItem(id + "-" + name);
            }
        } catch (Exception e) {
        }
    }
